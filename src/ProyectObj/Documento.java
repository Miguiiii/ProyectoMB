package ProyectObj;

/**
 *
 * @author usuario
 */
public class Documento {

    private String name;
    private String type;
    private int size;

    public Documento(String name, String type, int size) {
        this.name = name;
        this.type = type;
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public int getSize() {
        return size;
    }

    
}
